
weather = input("What is the weather like today? (sunny/rainy/cold): ")

if weather == "sunny":
    print("Wear a t-shirt and sunglasses.")

elif weather == "rainy":
    print("Don't forget your umbrella.")

elif weather == "cold":
    print("Make sure you have a coat and a scarf.")

else:
    print("Sorry, I do n't have reccommendation for this weather.")
